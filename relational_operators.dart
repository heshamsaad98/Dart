// Relational Operators

void main() {
  int x = 10;
  int y = 5;
  var result;

  // Less than operator
  result = x < y;
  print(result);

// Less than or equal operator
  result = x <= y;
  print(result);

  // Greater than operator
  result = x > y;
  print(result);

// Greater than or equal operator
  result = x >= y;
  print(result);
}
