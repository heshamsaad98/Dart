// DateTime Properties

void main() {
  var time = DateTime.now();
  print(time);
  print(time.timeZoneName);
  print(time.timeZoneOffset);
  print(time.isUtc);
  print(time.year);
  print(time.month);
  print(time.day);
  print(time.weekday);
  print(time.hour);
  print(time.minute);
  print(time.millisecond);
  print(time.microsecond);
  print(time.second);
  print(time.millisecondsSinceEpoch);
  print(time.microsecondsSinceEpoch);
  print(time.runtimeType);
}
